String.prototype.emoji=function(){
    var string=this.toString();
    for(var icon_text in emoji_utf8) {
        while(-1!=string.indexOf(icon_text))
        {
            string=string.replace(icon_text,'<span class="emoji emoji'+emoji_utf8[icon_text]+'"></span>');
        }
    }

    for(var icon_text in QQFaceMap)
    {
        while(-1!=string.indexOf('[' + icon_text + ']'))
        {
            string=string.replace('[' + icon_text + ']','<span class="qqemoji qqemoji'+QQFaceMap[icon_text]+'"></span>');
        }
    }
    return string;
};

