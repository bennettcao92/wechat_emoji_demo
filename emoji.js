function selectEmoticon(e) {
    var a = e.target;
    if ("A" == a.tagName) {
        var i = a.innerText || a.textContent;
        c = a.getAttribute("type");
        switch (c) {
            case "qq":
                i = "[" + i + "]";
                i = getEmoticonByText(i);
                insertToEditArea(i);
                break;
            case "emoji":
                i = "<" + i + ">";
                i = getEmoticonByText(i);
                insertToEditArea(i);
                break;
        }
        e.preventDefault()
    }
}

function insertToEditArea(element) {
    $('.message-box').append(element);
}

function getEmoticonByText(e) {
    var t;
    if (e.indexOf("<") > -1) {
        if (t = QQFaceMap[e])
            return genEmoticonHTML("emoji emoji" + t, EmojiCodeMap[t])
    } else if (t = QQFaceMap[e.replace(/\[|\]/g, "")])
        return genEmoticonHTML("qqemoji qqemoji" + t, e);
    return null;
};

function genEmoticonHTML(e, t) {
    return '<img class="' + e + '" text="' + t + (t.indexOf(CONSTANT.MM_EMOTICON_WEB) > -1 ? "" : CONSTANT.MM_EMOTICON_WEB) + '" src="' + CONSTANT.RES_IMG_PLACEHOLDER + '" />';
};

function getEditEmojiList() {
    var qqEmojiList, emojiList, list;
    qqEmojiList = $('.message-box').find('.qqemoji');
    emojiList = $('.message-box').find('.emoji');
    list = $.merge(qqEmojiList, emojiList);
    if (list.length) {
        list.each(function(index, element) {
            replaceEmojiToText(element);
        });
    }
}

function replaceEmojiToText(element) {
    text = $(element).attr('text');
    index = text.indexOf(CONSTANT.EMOJI_WEB_TEXT)
    if (index > -1) {
        text = text.substr(0, index);
    }
    $(element).replaceWith(text);
}



